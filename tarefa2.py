import random #importacao
from math import sqrt #importacaodemodulo

# Gera um numero aleatorio inteiro entre 1 e 20
sorteia_num = random.randint(1, 20)

# Calcula a raiz quadrada de 44
raiz_de_44 = sqrt(44)

# Multiplica o número aleatorio pela raiz quadrada de 44
resultado = sorteia_num * raiz_de_44

# Exibe o resultado
print("Número aleatório:", sorteia_num)
print("Raiz quadrada de 44:", raiz_de_44)
print("Resultado da multiplicação:", resultado)