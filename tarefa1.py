num1 = float(input("Qual é o valor do 1° número?? ")); #recebe numero um

num2 = float(input("Qual é o valor do 2° número??")); #recebe numero dois

Soma = num1 + num2; #soma os valores inseridos
Subtracao = num1 - num2; #subtrai os valores inseridos
Multiplicacao = num1 * num2; #multiplica os valores inseridos
Divisao = num1/num2; #divide os valores inseridos
Potencia = num1**num2; #potencia dos valores inseridos

print("A soma dos dois números é", Soma,", a subtração do segundo número pelo primeiro número é ", Subtracao,", divisão entre o primeiro número e o segundo número resulta em ", Divisao,", a multiplicação entre eles é ", Multiplicacao," e a potenciação entre eles, sendo o primeiro número a base e o segundo número o expoente, é de ", Potencia, ".") #impressao das operacoes